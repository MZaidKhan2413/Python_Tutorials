"""If our conditional statement have very small logic or line of code is not much big ;i.e, 2-3 LOC
    then we use this way of if...else"""

# Program to find Greatest among 2 numbers :
a = 10
b = 20
print(f"a={a} is greater !") if a>b else print(f"b={b} is greater !") if b>a else print("Both numbers are equall !")

# As we can see in above example, whole logic is completed in just one line